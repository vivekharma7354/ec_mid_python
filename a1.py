k=input()
# 1. Title is used Format String in standard format i.e. First Letter Capital And The Others Small. 
print('1. Using Title :- ',k.title())
# 2. Upper is used to format the whole string to upper case.
print('2. Using Upper :- ',k.upper())
# 3. Lower is used to format the whole string to lower case.
print('3. USing Lower :- ',k.lower())
# 4. Swapcase is used to reverse the case of each letter of the specified string.
print('4. USing Swapcase :- ',k.swapcase())
